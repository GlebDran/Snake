using System; // подключение стандартной библиотеки .NET (здесь не используется напрямую, но может пригодиться)
using System.Collections.Generic; // для использования списка List<>

namespace Snake // пространство имён игры, объединяющее все классы проекта
{
    // Абстрактный базовый класс для всех "фигур" на экране (например: змейка, стены, бонусы)
    public class Figure
    {
        // Защищённое поле: список точек, из которых состоит фигура (например, стенка или тело змейки)
        // protected означает, что это поле доступно внутри этого класса и его наследников (например, Snake)
        protected List<Point> pList = new List<Point>();

        // Метод для отрисовки всей фигуры на экране
        public void Draw()
        {
            // Проходимся по каждой точке фигуры и вызываем метод её отрисовки
            foreach (Point p in pList)
            {
                p.Draw(); // метод Draw() находится в классе Point и рисует символ в нужной позиции
            }
        }

        // Метод проверяет: попадает ли переданная точка в текущую фигуру
        public bool IsHit(Point point)
        {
            // Перебираем все точки фигуры
            foreach (Point p in pList)
            {
                // Если координаты одной из точек совпадают с переданной — значит, есть "столкновение"
                if (p.IsHit(point)) // метод IsHit внутри класса Point
                    return true;   // есть совпадение — возвращаем true
            }
            return false; // ни одна точка не совпала — значит, столкновения нет
        }
    }
}
