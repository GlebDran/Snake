using System.Collections.Generic; // подключаем библиотеку для использования списка List<>

namespace Snake // пространство имён проекта, объединяющее все игровые классы
{
    // Класс описывает горизонтальную линию — например, верхнюю или нижнюю стенку поля
    // Он наследуется от базового класса Figure, где уже реализованы Draw() и IsHit()
    public class HorizontalLine : Figure
    {
        // Конструктор принимает координаты начала и конца по оси X, высоту Y и символ линии
        public HorizontalLine(int xLeft, int xRight, int y, char sym)
        {
            pList = new List<Point>(); // создаём новый список точек для хранения всей линии

            // Проходим по X от xLeft до xRight, создавая точки с одинаковой высотой Y
            for (int x = xLeft; x <= xRight; x++)
            {
                // Создаём точку по координатам X и Y с заданным символом (например '+')
                Point p = new Point(x, y, sym);

                // Добавляем точку в список линии
                pList.Add(p);
            }

            // После этого список pList будет содержать всю горизонтальную линию из точек
            // А отрисовка линии будет происходить с помощью метода Draw() из Figure
        }
    }
}
